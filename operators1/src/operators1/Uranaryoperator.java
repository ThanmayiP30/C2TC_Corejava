package operators1;

public class Uranaryoperator {

	public static void main(String[] args) {
		int a=10;
		int b=11;
		System.out.println(a++);
		a++;
		System.out.println(a);
		System.out.println(++a);
		++a;
		System.out.println(a);
		System.out.println(b--);
		b--;
		System.out.println(b);
		System.out.println(--b);
		--b;
		System.out.println(b);


		
	}

}
